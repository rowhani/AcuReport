#------------------------------------------------------------------------
# "_getConvertedShape" : 
#------------------------------------------------------------------------

def _getConvertedShape( coordinates, coordIndex ):

    vertProp = SoVertexProperty( )
    vertProp.vertex.setValues( 0, coordinates )

    shape = SoIndexedFaceSet( )
    shape.vertexProperty.setValue( vertProp )
    shape.coordIndex.setValues( 0, coordIndex )

    return shape            
        
#------------------------------------------------------------------------
# "convertSphere" : 
#------------------------------------------------------------------------

def convertSphere( sphere ):

    radius = sphere.radius.getValue( )

    coordinates = numarray.array( SphereCoordinates )
    
    if radius != SphereDefaultRadius:
        coordinates = coordinates * ( radius / SphereDefaultRadius )

    return _getConvertedShape( coordinates, SphereCoordIndex )

#------------------------------------------------------------------------
# "convertCube" : 
#------------------------------------------------------------------------

def convertCube( cube ):

    width   = cube.width.getValue( )
    height  = cube.height.getValue( )
    depth   = cube.depth.getValue( )

    coordinates = numarray.array( CubeCoordinates )

    if width != CubeDefaultWidth:
        coordinates[:, 0] = coordinates[:, 0] * ( width / CubeDefaultWidth )
        
    if height != CubeDefaultHeight:
        coordinates[:, 1] = coordinates[:, 1] * ( height / CubeDefaultHeight )
        
    if depth != CubeDefaultDepth:
        coordinates[:, 2] = coordinates[:, 2] * ( depth / CubeDefaultDepth )
    
    return _getConvertedShape( coordinates, CubeCoordIndex )

#------------------------------------------------------------------------
# "convertCone" : 
#------------------------------------------------------------------------

def convertCone( cone ):

    bottomRadius    = cone.bottomRadius.getValue( )    
    height          = cone.height.getValue( )

    coordinates = numarray.array( ConeCoordinates )

    if bottomRadius != ConeDefaultBottomRadius:
        coordinates[:, 0] = coordinates[:, 0] * ( bottomRadius / ConeDefaultBottomRadius )
        coordinates[:, 2] = coordinates[:, 2] * ( bottomRadius / ConeDefaultBottomRadius )
        
    if height != ConeDefaultHeight:
        coordinates[:, 1] = coordinates[:, 1] * ( height / ConeDefaultHeight )
       
    return _getConvertedShape( coordinates, ConeCoordIndex )

#------------------------------------------------------------------------
# "convertCylinder" : 
#------------------------------------------------------------------------

def convertCylinder( cylinder ):

    radius  = cylinder.radius.getValue( )    
    height  = cylinder.height.getValue( )

    coordinates = numarray.array( CylinderCoordinates )

    if radius != CylinderDefaultRadius:
        coordinates[:, 0] = coordinates[:, 0] * ( radius / CylinderDefaultRadius )
        coordinates[:, 2] = coordinates[:, 2] * ( radius / CylinderDefaultRadius )
        
    if height != CylinderDefaultHeight:
        coordinates[:, 1] = coordinates[:, 1] * ( height / CylinderDefaultHeight )
       
    return _getConvertedShape( coordinates, CylinderCoordIndex )

